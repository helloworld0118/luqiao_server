package com.core.util.model;

public class LaborModel {
	private int project;
	private String program;
	private String node;
	private String procedure;
	private int foreman;
	private String foremanName;
	private int worker;
	private int count;
	private int baseType;
	private String baseTypeName;
	private int price;
	private int worker_nums;
	private String date;
	private String remark;
	public int getProject() {
		return project;
	}
	public void setProject(int project) {
		this.project = project;
	}
	public String getProgram() {
		return program;
	}
	public void setProgram(String program) {
		this.program = program;
	}
	public String getNode() {
		return node;
	}
	public void setNode(String node) {
		this.node = node;
	}
	public String getProcedure() {
		return procedure;
	}
	public void setProcedure(String procedure) {
		this.procedure = procedure;
	}
	public int getForeman() {
		return foreman;
	}
	public void setForeman(int foreman) {
		this.foreman = foreman;
	}
	public String getForemanName() {
		return foremanName;
	}
	public void setForemanName(String foremanName) {
		this.foremanName = foremanName;
	}
	public int getWorker() {
		return worker;
	}
	public void setWorker(int worker) {
		this.worker = worker;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getBaseType() {
		return baseType;
	}
	public void setBaseType(int baseType) {
		this.baseType = baseType;
	}
	public String getBaseTypeName() {
		return baseTypeName;
	}
	public void setBaseTypeName(String baseTypeName) {
		this.baseTypeName = baseTypeName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getWorker_nums() {
		return worker_nums;
	}
	public void setWorker_nums(int worker_nums) {
		this.worker_nums = worker_nums;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
